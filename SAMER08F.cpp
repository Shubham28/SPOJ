using namespace std;

inline void Input(int &N)
{
	int ch;
	N=0;

	while((ch<'0' || ch>'9') && ch!='-' && ch!=EOF)
		ch=getchar();

	do
		N=(N<<3)+(N<<1)+(ch-'0');
	while((ch=getchar())>='0' && ch<='9');

	return;
}

int main()
{
	int N;
	Input(N);
	while(N!=0){
		printf("%d\n",N*(N+1)*(2*N+1)/6);

		Input(N);
	}
	return 0;
}
